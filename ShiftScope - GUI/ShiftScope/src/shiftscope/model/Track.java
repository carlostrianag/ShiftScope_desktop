public class Track {
    private int idSong;
    private int idTrackList;

    /**
     * @return the idSong
     */
    public int getIdSong() {
        return idSong;
    }

    /**
     * @param idSong the idSong to set
     */
    public void setIdSong(int idSong) {
        this.idSong = idSong;
    }

    /**
     * @return the idTrackList
     */
    public int getIdTrackList() {
        return idTrackList;
    }

    /**
     * @param idTrackList the idTrackList to set
     */
    public void setIdTrackList(int idTrackList) {
        this.idTrackList = idTrackList;
    }
    
}
